package division;

public class Division {
	
	public static double division2Reales(double num1, double num2) {
		double division;
		division = num1 / num2;
		System.out.println(division);
		return division;
	}
	
	public static double division2Enteros(int num1, int num2) {
		double division;
		division = num1 / num2;
		System.out.println(division);
		return division;
	}
	
	public static double inversoNumero(double num1) {
		double inverso;
		inverso = 1 / num1;
		System.out.println(inverso);
		return inverso;
	}
	
	public static double raizCuadradaNumero(double num1) {
		double raiz;
		raiz = Math.sqrt(num1);
		System.out.println(raiz);
		return raiz;
	}

}
